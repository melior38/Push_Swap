int	sort_case(t_env *env)
{
	int	size;
	int sort_case;

	size = lst_len(env->a) + 1;
	if (size >= 4 && size <= 15)
		sort_case = 1;
	else if (size >= 16 && size <= 100)
		sort_case = 2;
	else if (size >= 101 && size <= 300)
		sort_case = 3;
	else if (size >= 301)
		sort_case = 4;
	else if (size >= 501)
		sort_case = 5;
	else 
		sort_case = 0;
	return (sort_case);
}

void	sort_3(t_env *env)
{
	int	max;
	int min;

	min = x_min(env->a, 0);
	// printf("%d\n", min);
	max = x_min(env->a, 3);
	// printf("%d\n", max);
	if (env->a->data == max)
	{
		ra(env);
		if (env->a->next->data == min)
			sa(env);
	}
	else if (env->a->data == min)
	{
		if (env->a->next->data == max)
		{
			rra(env);
			sa(env);
		}
	}
	else
	{
		if (env->a->next->data == min)
			sa(env);
		else
			rra(env);
	}
}

